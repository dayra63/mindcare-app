    private void btnGoRegistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGoRegistActionPerformed
        String username = txtUsernameRegis.getText().trim();
        String email = txtEmailRegist.getText().trim();
        String password = new String(txtPasswordRegist.getPassword()).trim();
        
        if (username.isEmpty()  || email.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (!email.contains("@") || !email.contains(".")) {
             JOptionPane.showMessageDialog(this, "Format email tidak valid!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (conn == null) {
            JOptionPane.showMessageDialog(this, "Database connection failed. Please check your configuration.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (isUsernameExists(username)) {
             JOptionPane.showMessageDialog(this, "Username sudah digunakan. Silakan pilih username lain.", "Error", JOptionPane.ERROR_MESSAGE);
            txtUsernameRegis.requestFocus();
            return;
        }
        
        if (isEmailExists(email)) {
             JOptionPane.showMessageDialog(this, "Email sudah terdaftar. Silakan gunakan email lain.", "Error", JOptionPane.ERROR_MESSAGE);
            txtEmailRegist.requestFocus();
            return;
        }
        
        if (password.length() < 6) {
             JOptionPane.showMessageDialog(this, "Password minimal 6 karakter!", "Error", JOptionPane.ERROR_MESSAGE);
            txtPasswordRegist.requestFocus();
            return;
        }
        
        boolean success = registerUser(username, email, password);
        
        if (success) {
            JOptionPane.showMessageDialog(this, "Registrasi berhasil! Silakan login dengan akun baru Anda.", "Success", JOptionPane.INFORMATION_MESSAGE);
            
            txtUsernameRegis.setText("");
            txtEmailRegist.setText("");
            txtPasswordRegist.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Registrasi gagal. Silakan coba lagi.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGoRegistActionPerformed

